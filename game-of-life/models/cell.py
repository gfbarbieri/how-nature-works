    # INITIALIZATION
    ###########################################################################

    def setup(self):
        """
        Initializes the generic agent's attribute `status`.
        """

        # Set status attribute.
        # self.status = self.model.random.randint(0, 1)
        self.status = self.model.nprandom.choice(0, 1, p=[0.90, 0.10])
